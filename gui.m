% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
I = im2double(imread(strcat(handles.pathname,handles.filename)));
[h,w,d] = size(I);
if(h > 1000)
    I = imresize(I, .5);
end
axes(handles.axes6);
cla;
axes(handles.axes3);
cla;

%[h, w] = size(I);
%if(h > 700 || w > 700)
 %   I = imresize(I, .5);
%end

imshow(I);

set(handles.text6, 'String', 'Calculating...please be patient. Processing takes approximately 1 minute');

pause(3);
option = 1;
sensitivity = .15;

switch get(handles.popupmenu2,'Value') 
    case 1
        option = 1;
    case 2
        option = 1;
    case 3
        option = 2;
    case 4
        option = 3;
    otherwise
end

switch get(handles.popupmenu3,'Value') 
    case 1
        sensitivity = -2.2;
    case 2
        sensitivity = -2.4;
    case 3
        sensitivity = -2.2;
    case 4
        sensitivity = -1.9;
    otherwise
end


o = regFind(I,option, sensitivity);
axes(handles.axes6);

imshow(o);

set(handles.text6, 'String', 'Solo cup identification complete.');

guidata(hObject, handles);




